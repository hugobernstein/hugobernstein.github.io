    ◊br[]
    围绕一个概念，总会涉及这几类人：其缔造者、反对者、追随者和旁观者。站在不同的立场来回顾这个概念会触发不同的思考。以精准医疗为例，奥巴马前总统提出这一概念的时候，反对他的人们就不分青红皂白地反对起来了。追随者也有他们自己的举动。当读了一些文档之后，人们或许会意识到这并不是一个全新的概念，旁观者会在进一步的行动之前思考概念缔造者们的动机。
    于是，我也会尽量不充满激情地反对一件事。如果对这几个角色进行排序，我最希望做的是一个可行的、全新的概念的缔造者，其次是一个清醒的概念追随者，然后是好奇的旁观者，之后是针对坏概念的反对者。无论何时，都不应该针对人，比如质疑巴拉克(侯赛因?)奥巴马。
