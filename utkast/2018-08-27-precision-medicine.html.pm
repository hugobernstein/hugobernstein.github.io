    炒概念是一个存在的现象，例如精准医疗、液态活检、转化医学或个体化医疗，先不管它们是否合理，我选择退一步想想更大而无用的东西。
    ◊br[]
    我赞同韩健教授的观点：精准医疗的最终收益者应该是病人。可是至少在现阶段，病人受益非常有限。首先，能够被精准使用的药物非常有限。精准医疗的口号就是把正确的药，在正确的时间，用在正确的病人身上。如果能够使用的药物本身就是有限的（绝大多数用于精准医疗的抗肿瘤药物在内地/大陆市场都没有），诊断技术上面再打点折扣，科学性（肿瘤的进化和异质性）上再打些折扣，价格上再给些压力，最后能够受益的病人就变成很少数的一些人了。
    "通常，概念的后面都有利益集团。"
